count = 0  # Initialize a counter to keep track of the even numbers
number = 2  # Start with the first even number

while count < 10:  # Continue the loop until we have printed 10 even numbers
    print(number)  # Print the current even number
    number += 2  # Increment the number by 2 to get the next even number
    count += 1  # Increment the counter

# The loop will exit after printing 10 even numbers
